test_runner_json_artifact_records_failure_reason() {
  local tmpdir
  tmpdir="$(mktemp -d)"
  setup_runner_fixture "${tmpdir}"
  install_fake_git_short_sha "${tmpdir}"

  setup_e2e_fixture "${tmpdir}" \
    "e2e_test/smoke/fail_test.dart" \
    "e2e_test/smoke/pass_test.dart"

  cat > "${tmpdir}/scripts/dev/patrol_fast.sh" <<'SCRIPT'
#!/usr/bin/env bash
target=""
while [[ $# -gt 0 ]]; do
  case "$1" in
    -t)
      target="$2"
      shift 2
      ;;
    *)
      shift
      ;;
  esac
done

if [[ "${target}" == "e2e_test/smoke/fail_test.dart" ]]; then
  echo "fixture failure output" >&2
  exit 1
fi

echo "fixture success output"
exit 0
SCRIPT
  chmod +x "${tmpdir}/scripts/dev/patrol_fast.sh"

  local output=""
  local exit_code=0
  output="$(
    cd "${tmpdir}" &&
    PATH="${tmpdir}/bin:${PATH}" \
    bash "./scripts/dev/run_ios_signoff_suite.sh" --env dev --device "Fixture iPhone" 2>&1
  )" || exit_code=$?

  assert_eq "1" "${exit_code}" "runner exits non-zero when any signoff test fails"
  assert_contains "${output}" "fixture failure output" \
    "runner still streams failing patrol output"

  local json_file
  json_file="$(ls "${tmpdir}"/tmp/signoff/signoff_*_deadbee.json 2>/dev/null | head -n 1)"
  if [[ -z "${json_file}" ]]; then
    echo "FAIL: runner writes JSON artifact after a failed patrol test"
    failures=$((failures + 1))
    rm -rf "${tmpdir}"
    return
  fi

  local parsed_output
  parsed_output="$(python3 - <<'PY' "${json_file}"
import json
import sys

data = json.load(open(sys.argv[1]))
print(data["patrol_output_dir"])
failed = next(item for item in data["results"] if item["test"] == "e2e_test/smoke/fail_test.dart")
print(failed.get("reason", ""))
print(failed.get("duration_ms", -1))
PY
)"

  local patrol_output_dir
  patrol_output_dir="$(printf '%s\n' "${parsed_output}" | sed -n '1p')"
  local failure_reason
  failure_reason="$(printf '%s\n' "${parsed_output}" | sed -n '2p')"
  local failure_duration_ms
  failure_duration_ms="$(printf '%s\n' "${parsed_output}" | sed -n '3p')"

  assert_contains "${patrol_output_dir}" "${tmpdir}/tmp/signoff/patrol_" \
    "runner JSON artifact reports patrol_output_dir"
  assert_success "runner creates patrol_output_dir" test -d "${patrol_output_dir}"
  assert_contains "${failure_reason}" "patrol exited with status 1" \
    "runner JSON artifact records a per-test failure reason"
  assert_success "runner JSON artifact records a per-test duration" \
    test "${failure_duration_ms}" -ge 0

  rm -rf "${tmpdir}"
}
