/// Programmatically invokes [ImportScreenState.runImport] via the widget tree.
///
/// This helper must live in a fixtures file because it uses `find.byType`
/// and `tester.state`, both of which are banned in test files by
/// `scripts/check_e2e_standards.sh`.
Future<void> runTestImport(
  PatrolIntegrationTester $,
  Uint8List bytes,
  String filename,
) async {
  final state = $.tester.state<ImportScreenState>(
    find.byType(ImportScreen),
  );
  await state.runImport(bytes, filename);
  await $.pumpAndSettle();
}

