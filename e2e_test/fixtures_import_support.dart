/// Builds a deterministic ZIP export containing FIT activities.
Uint8List buildTestZipBytes({int activityCount = 6}) {
  if (activityCount < 1) {
    throw ArgumentError.value(activityCount, 'activityCount', 'must be >= 1');
  }

  final archive = Archive();
  for (var i = 0; i < activityCount; i++) {
    final fitBytes = buildFitBytes(
      records: [
        FitTestRecord(
          timestampMs:
              fitBaseTimestamp + Duration(minutes: i * 3).inMilliseconds,
          latitude: testLatitude + i * 0.0001,
          longitude: testLongitude + i * 0.0001,
          altitude: 10.0 + i,
        ),
        FitTestRecord(
          timestampMs:
              fitBaseTimestamp + Duration(minutes: i * 3 + 1).inMilliseconds,
          latitude: testLatitude + i * 0.0001 + 0.0012,
          longitude: testLongitude + i * 0.0001 + 0.0012,
          altitude: 20.0 + i,
        ),
        FitTestRecord(
          timestampMs:
              fitBaseTimestamp + Duration(minutes: i * 3 + 2).inMilliseconds,
          latitude: testLatitude + i * 0.0001 + 0.0024,
          longitude: testLongitude + i * 0.0001 + 0.0024,
          altitude: 30.0 + i,
        ),
      ],
      sport: Sport.running,
    );
    archive.add(
      ArchiveFile.bytes(
        'activities/activity_${i.toString().padLeft(2, '0')}.fit',
        fitBytes,
      ),
    );
  }

  return Uint8List.fromList(ZipEncoder().encodeBytes(archive));
}

/// Returns the exact history-row title generated by the deterministic FIT
/// import fixtures in this file.
///
/// This keeps import E2E assertions aligned with the shared title formatter
/// and the single canonical FIT timestamp instead of relying on broad text
/// matches like `textContaining('Run')`.
String expectedImportedRunTitleForTesting({
  Duration startOffset = Duration.zero,
}) {
  final startedAt = DateTime.fromMillisecondsSinceEpoch(
    fitBaseTimestamp + startOffset.inMilliseconds,
    isUtc: true,
  );
  return generateDefaultActivityTitle(startedAt: startedAt);
}

/// Configures FilePicker to return a deterministic single picked import file.
void setNextPickedImportFile({
  required String name,
  required Uint8List bytes,
}) {
  FilePicker.platform = _FixtureFilePicker(
    result: FilePickerResult([
      PlatformFile(
        name: name,
        size: bytes.length,
        bytes: bytes,
      ),
    ]),
  );
}

/// Restores the process-global FilePicker platform implementation.
void restoreFilePickerPlatform() {
  FilePicker.platform = _defaultFilePickerPlatform;
}

